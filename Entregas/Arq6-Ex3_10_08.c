void mostra_car(unsigned char lin) {
	P0 = caract[lin][0];
	P1 = caract[lin][1];
	P2 = caract[lin][2];
	P3 = caract[lin][3];
}

void delay_ms(unsigned int t) {
	TMOD |=  0x01;
	TMOD &= ~0x02;

	for(;t>0;t--) {
		TR0 = 0;
		TF0 = 0;
		TL0 = 0x58;
		TH0 = 0x9E;

		TR0 = 1;
		while(TF0 == 0);
	}
}

void main (void) {
	int i;
	Init_Device();
	SFRPAGE = LEGACY_PAGE;

	for(i=0; i<43; i++) {
		delay_ms(500);
		mostra_car(i);
	}

	delay_ms(500);
	P0= 0xff;
	P1= 0xff;
	P2= 0xff;
	P3= 0xff;
}
